#include "gcm.h"

#define GCM_GET_BE32(B, I)                                                                         \
    (((uint32_t) (B)[(I) + 0u] << 24) | ((uint32_t) (B)[(I) + 1u] << 16) |                         \
     ((uint32_t) (B)[(I) + 2u] << 8) | ((uint32_t) (B)[(I) + 3u]))

#define GCM_PUT_BE32(B, V, I)                                                                      \
    do                                                                                             \
    {                                                                                              \
        (B)[(I) + 0u] = (unsigned char) ((uint32_t) (V) >> 24);                                    \
        (B)[(I) + 1u] = (unsigned char) ((uint32_t) (V) >> 16);                                    \
        (B)[(I) + 2u] = (unsigned char) ((uint32_t) (V) >> 8);                                     \
        (B)[(I) + 3u] = (unsigned char) ((uint32_t) (V));                                          \
    } while (0)

#define GCM_GET_BE64(B, I)                                                                         \
    (((uint64_t) GCM_GET_BE32((B), (I)) << 32) | ((uint64_t) GCM_GET_BE32((B), (I) + 4u)))

#define GCM_PUT_BE64(B, V, I)                                                                      \
    do                                                                                             \
    {                                                                                              \
        GCM_PUT_BE32((B), (uint32_t) ((uint64_t) (V) >> 32), (I));                                 \
        GCM_PUT_BE32((B), (uint32_t) ((uint64_t) (V)), (I) + 4u);                                  \
    } while (0)

#define GCM_LO_NIBBLE(X) ((uint8_t) ((X) & 0x0Fu))
#define GCM_HI_NIBBLE(X) ((uint8_t) ((X) >> 4))

#define GCM_SHIFT4(ZH, ZL)                                                                         \
    do                                                                                             \
    {                                                                                              \
        uint8_t rem = GCM_LO_NIBBLE(ZL);                                                           \
        (ZL) = ((ZH) << 60) | ((ZL) >> 4);                                                         \
        (ZH) >>= 4;                                                                                \
        (ZH) ^= (uint64_t) gcm_last4[rem] << 48;                                                   \
    } while (0)

#define GCM_STANDARD_IV_SIZE 12u
#define GCM_COUNTER_SIZE 4u
#define GCM_MIN_TAG_SIZE 4u
#define GCM_DEFAULT_TAG_SIZE GCM_BLOCK_SIZE

static const uint16_t gcm_last4[GCM_NIBBLE_TABLE_SIZE] = {
    0x0000u, 0x1C20u, 0x3840u, 0x2460u, 0x7080u, 0x6CA0u, 0x48C0u, 0x54E0u,
    0xE100u, 0xFD20u, 0xD940u, 0xC560u, 0x9180u, 0x8DA0u, 0xA9C0u, 0xB5E0u};

static void math_zeroize(void *v, size_t n)
{
    volatile unsigned char *p = (volatile unsigned char *) v;

    while (n-- != 0u)
    {
        *p++ = 0u;
    }
}

void math_gcm_init(math_gcm_context *ctx)
{
    macsec_assert(ctx != NULL);

    memset(ctx, 0, sizeof(*ctx));
}

static int gcm_aes_encrypt_block(math_gcm_context *ctx, const unsigned char input[GCM_BLOCK_SIZE],
                                 unsigned char output[GCM_BLOCK_SIZE])
{
    return math_aes_encrypt(&ctx->aes_ctx, input, output);
}

static void gcm_mult(math_gcm_context *ctx, const unsigned char x[GCM_BLOCK_SIZE],
                     unsigned char output[GCM_BLOCK_SIZE])
{
    int i;
    uint8_t lo;
    uint8_t hi;
    uint64_t zh;
    uint64_t zl;

    lo = GCM_LO_NIBBLE(x[GCM_BLOCK_SIZE - 1u]);
    zh = ctx->HH[lo];
    zl = ctx->HL[lo];

    for (i = (int) GCM_BLOCK_SIZE - 1; i >= 0; i--)
    {
        lo = GCM_LO_NIBBLE(x[i]);
        hi = GCM_HI_NIBBLE(x[i]);

        if (i != (int) GCM_BLOCK_SIZE - 1)
        {
            GCM_SHIFT4(zh, zl);
            zh ^= ctx->HH[lo];
            zl ^= ctx->HL[lo];
        }

        GCM_SHIFT4(zh, zl);
        zh ^= ctx->HH[hi];
        zl ^= ctx->HL[hi];
    }

    GCM_PUT_BE64(output, zh, 0u);
    GCM_PUT_BE64(output, zl, 8u);
}

static void gcm_increment_counter(unsigned char counter[GCM_BLOCK_SIZE])
{
    size_t i;

    for (i = GCM_BLOCK_SIZE; i > GCM_BLOCK_SIZE - GCM_COUNTER_SIZE; i--)
    {
        counter[i - 1u]++;

        if (counter[i - 1u] != 0u)
        {
            break;
        }
    }
}

int math_gcm_setkey(math_gcm_context *ctx, const unsigned char *key, unsigned int keybits)
{
    int ret;
    int i;
    int j;
    uint64_t vl;
    uint64_t vh;
    unsigned char h[GCM_BLOCK_SIZE];

    macsec_assert(ctx != NULL);
    macsec_assert(key != NULL);

    if (keybits != 128u && keybits != 192u && keybits != 256u)
    {
        return MATH_ERR_GCM_BAD_INPUT;
    }

    math_aes_free(&ctx->aes_ctx);
    math_aes_init(&ctx->aes_ctx);

    ret = math_aes_setenckey(&ctx->aes_ctx, key, keybits);
    if (ret != 0)
    {
        return ret;
    }

    memset(h, 0, sizeof(h));

    ret = gcm_aes_encrypt_block(ctx, h, h);
    if (ret != 0)
    {
        math_zeroize(h, sizeof(h));
        return ret;
    }

    vh = GCM_GET_BE64(h, 0u);
    vl = GCM_GET_BE64(h, 8u);

    ctx->HL[8] = vl;
    ctx->HH[8] = vh;
    ctx->HH[0] = 0u;
    ctx->HL[0] = 0u;

    for (i = 4; i > 0; i >>= 1)
    {
        uint32_t reduction = (uint32_t) (vl & 1u) * 0xE1000000u;

        vl = (vh << 63) | (vl >> 1);
        vh = (vh >> 1) ^ ((uint64_t) reduction << 32);

        ctx->HL[i] = vl;
        ctx->HH[i] = vh;
    }

    for (i = 2; i <= 8; i *= 2)
    {
        uint64_t *table_l = ctx->HL + i;
        uint64_t *table_h = ctx->HH + i;

        vh = *table_h;
        vl = *table_l;

        for (j = 1; j < i; j++)
        {
            table_h[j] = vh ^ ctx->HH[j];
            table_l[j] = vl ^ ctx->HL[j];
        }
    }

    math_zeroize(h, sizeof(h));
    return 0;
}

static int gcm_starts(math_gcm_context *ctx, int mode, const unsigned char *iv, size_t iv_len,
                      const unsigned char *add, size_t add_len)
{
    int ret;
    unsigned char tmp[GCM_BLOCK_SIZE];
    size_t i;
    size_t use_len;
    const unsigned char *p;

    macsec_assert(ctx != NULL);
    macsec_assert(iv != NULL);

    if (mode != MATH_GCM_ENCRYPT && mode != MATH_GCM_DECRYPT)
    {
        return MATH_ERR_GCM_BAD_INPUT;
    }

    if (add_len != 0u && add == NULL)
    {
        return MATH_ERR_GCM_BAD_INPUT;
    }

    if (iv_len == 0u || (((uint64_t) iv_len) >> 61) != 0u || (((uint64_t) add_len) >> 61) != 0u)
    {
        return MATH_ERR_GCM_BAD_INPUT;
    }

    memset(ctx->y, 0, sizeof(ctx->y));
    memset(ctx->buf, 0, sizeof(ctx->buf));

    ctx->mode = mode;
    ctx->len = 0u;
    ctx->add_len = add_len;

    if (iv_len == GCM_STANDARD_IV_SIZE)
    {
        memcpy(ctx->y, iv, iv_len);
        ctx->y[GCM_BLOCK_SIZE - 1u] = 1u;
    }
    else
    {
        size_t remaining = iv_len;

        memset(tmp, 0, sizeof(tmp));
        GCM_PUT_BE64(tmp, (uint64_t) iv_len * 8u, 8u);

        p = iv;
        while (remaining > 0u)
        {
            use_len = (remaining < GCM_BLOCK_SIZE) ? remaining : GCM_BLOCK_SIZE;

            for (i = 0u; i < use_len; i++)
            {
                ctx->y[i] ^= p[i];
            }

            gcm_mult(ctx, ctx->y, ctx->y);
            remaining -= use_len;
            p += use_len;
        }

        for (i = 0u; i < GCM_BLOCK_SIZE; i++)
        {
            ctx->y[i] ^= tmp[i];
        }

        gcm_mult(ctx, ctx->y, ctx->y);
        math_zeroize(tmp, sizeof(tmp));
    }

    ret = gcm_aes_encrypt_block(ctx, ctx->y, ctx->base_ectr);
    if (ret != 0)
    {
        return ret;
    }

    p = add;
    while (add_len > 0u)
    {
        use_len = (add_len < GCM_BLOCK_SIZE) ? add_len : GCM_BLOCK_SIZE;

        for (i = 0u; i < use_len; i++)
        {
            ctx->buf[i] ^= p[i];
        }

        if (use_len == GCM_BLOCK_SIZE)
        {
            gcm_mult(ctx, ctx->buf, ctx->buf);
        }

        add_len -= use_len;
        p += use_len;
    }

    if ((ctx->add_len % GCM_BLOCK_SIZE) != 0u)
    {
        gcm_mult(ctx, ctx->buf, ctx->buf);
    }

    return 0;
}

/*
 * One-shot payload processing. This function is intentionally internal and
 * is called exactly once by math_gcm_crypt_and_tag().
 */
static int gcm_update(math_gcm_context *ctx, size_t length, const unsigned char *input,
                      unsigned char *output)
{
    int ret;
    unsigned char ectr[GCM_BLOCK_SIZE];
    size_t i;
    size_t use_len;
    const unsigned char *p;
    unsigned char *out_p;

    macsec_assert(ctx != NULL);

    if (length != 0u && (input == NULL || output == NULL))
    {
        return MATH_ERR_GCM_BAD_INPUT;
    }

    if (length != 0u)
    {
        uintptr_t input_addr = (uintptr_t) input;
        uintptr_t output_addr = (uintptr_t) output;

        if (output_addr > input_addr && output_addr - input_addr < length)
        {
            return MATH_ERR_GCM_BAD_INPUT;
        }
    }

    if (ctx->len + length < ctx->len || ctx->len + (uint64_t) length > 0xFFFFFFFE0ull)
    {
        return MATH_ERR_GCM_BAD_INPUT;
    }

    p = input;
    out_p = output;

    while (length > 0u)
    {
        use_len = (length < GCM_BLOCK_SIZE) ? length : GCM_BLOCK_SIZE;

        gcm_increment_counter(ctx->y);

        ret = gcm_aes_encrypt_block(ctx, ctx->y, ectr);
        if (ret != 0)
        {
            math_zeroize(ectr, sizeof(ectr));
            return ret;
        }

        for (i = 0u; i < use_len; i++)
        {
            if (ctx->mode == MATH_GCM_DECRYPT)
            {
                ctx->buf[i] ^= p[i];
            }

            out_p[i] = (unsigned char) (ectr[i] ^ p[i]);

            if (ctx->mode == MATH_GCM_ENCRYPT)
            {
                ctx->buf[i] ^= out_p[i];
            }
        }

        if (use_len == GCM_BLOCK_SIZE)
        {
            gcm_mult(ctx, ctx->buf, ctx->buf);
        }

        ctx->len += use_len;
        length -= use_len;
        p += use_len;
        out_p += use_len;
    }

    math_zeroize(ectr, sizeof(ectr));
    return 0;
}

static int gcm_finish(math_gcm_context *ctx, unsigned char *tag, size_t tag_len)
{
    unsigned char tmp[GCM_BLOCK_SIZE];
    size_t i;
    uint64_t data_bits;
    uint64_t add_bits;

    macsec_assert(ctx != NULL);
    macsec_assert(tag != NULL);

    if (tag_len > GCM_BLOCK_SIZE || tag_len < GCM_MIN_TAG_SIZE)
    {
        return MATH_ERR_GCM_BAD_INPUT;
    }

    data_bits = ctx->len * 8u;
    add_bits = ctx->add_len * 8u;

    if ((ctx->len % GCM_BLOCK_SIZE) != 0u)
    {
        gcm_mult(ctx, ctx->buf, ctx->buf);
    }

    memcpy(tag, ctx->base_ectr, tag_len);

    memset(tmp, 0, sizeof(tmp));
    GCM_PUT_BE64(tmp, add_bits, 0u);
    GCM_PUT_BE64(tmp, data_bits, 8u);

    for (i = 0u; i < GCM_BLOCK_SIZE; i++)
    {
        ctx->buf[i] ^= tmp[i];
    }

    gcm_mult(ctx, ctx->buf, ctx->buf);

    for (i = 0u; i < tag_len; i++)
    {
        tag[i] ^= ctx->buf[i];
    }

    math_zeroize(tmp, sizeof(tmp));
    return 0;
}

int math_gcm_crypt_and_tag(math_gcm_context *ctx, int mode, size_t length, const unsigned char *iv,
                           size_t iv_len, const unsigned char *add, size_t add_len,
                           const unsigned char *input, unsigned char *output, size_t tag_len,
                           unsigned char *tag)
{
    int ret;

    ret = gcm_starts(ctx, mode, iv, iv_len, add, add_len);
    if (ret != 0)
    {
        return ret;
    }

    ret = gcm_update(ctx, length, input, output);
    if (ret != 0)
    {
        return ret;
    }

    return gcm_finish(ctx, tag, tag_len);
}

int math_gcm_auth_decrypt(math_gcm_context *ctx, size_t length, const unsigned char *iv,
                          size_t iv_len, const unsigned char *add, size_t add_len,
                          const unsigned char *tag, size_t tag_len, const unsigned char *input,
                          unsigned char *output)
{
    int ret;
    int diff;
    size_t i;
    unsigned char check_tag[GCM_BLOCK_SIZE];

    macsec_assert(ctx != NULL);
    macsec_assert(tag != NULL);

    ret = math_gcm_crypt_and_tag(ctx, MATH_GCM_DECRYPT, length, iv, iv_len, add, add_len, input,
                                 output, tag_len, check_tag);
    if (ret != 0)
    {
        math_zeroize(check_tag, sizeof(check_tag));
        return ret;
    }

    diff = 0;
    for (i = 0u; i < tag_len; i++)
    {
        diff |= (int) (tag[i] ^ check_tag[i]);
    }

    math_zeroize(check_tag, sizeof(check_tag));

    if (diff != 0)
    {
        if (output != NULL)
        {
            math_zeroize(output, length);
        }

        return MATH_ERR_GCM_AUTH_FAILED;
    }

    return 0;
}

void math_gcm_free(math_gcm_context *ctx)
{
    macsec_assert(ctx != NULL);

    math_aes_free(&ctx->aes_ctx);
    math_zeroize(ctx, sizeof(*ctx));
}

