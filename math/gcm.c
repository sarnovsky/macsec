int math_gcm_self_test(math_gcm_context *ctx, unsigned char buf[GCM_BUF_SIZE], int verbose)
{
    int i;
    int key_size;
    int ret;
    int vector_index;
    int array_index = 0;
    unsigned char tag_buf[GCM_BLOCK_SIZE];

    for (key_size = 128; key_size <= 256; key_size += 64, array_index++)
    {
        for (i = 0; i < MAX_TESTS; i++)
        {
            vector_index = array_index * MAX_TESTS + i;

            if (pt_len[i] > GCM_BUF_SIZE)
            {
                if (verbose != 0)
                {
                    macsec_printf("failed: test vector plaintext too long\n");
                }
                return 1;
            }

            if (verbose != 0)
            {
                macsec_printf("  AES-GCM-%3d #%d (enc): ", key_size, i);
            }

            math_gcm_init(ctx);

            ret = math_gcm_setkey(ctx, key[key_index[i]], (unsigned int) key_size);
            if (ret == 0)
            {
                ret = math_gcm_crypt_and_tag(ctx, MATH_GCM_ENCRYPT, pt_len[i], iv[iv_index[i]],
                                             iv_len[i], additional[add_index[i]], add_len[i],
                                             pt[pt_index[i]], buf, GCM_DEFAULT_TAG_SIZE, tag_buf);
            }

            if (ret != 0 || memcmp(buf, ct[vector_index], pt_len[i]) != 0 ||
                memcmp(tag_buf, tag[vector_index], GCM_DEFAULT_TAG_SIZE) != 0)
            {
                if (verbose != 0)
                {
                    macsec_printf("failed\n");
                }

                math_gcm_free(ctx);
                return 1;
            }

            math_gcm_free(ctx);

            if (verbose != 0)
            {
                macsec_printf("passed\n");
            }

            if (verbose != 0)
            {
                macsec_printf("  AES-GCM-%3d #%d (dec): ", key_size, i);
            }

            math_gcm_init(ctx);

            ret = math_gcm_setkey(ctx, key[key_index[i]], (unsigned int) key_size);
            if (ret == 0)
            {
                ret = math_gcm_auth_decrypt(ctx, pt_len[i], iv[iv_index[i]], iv_len[i],
                                            additional[add_index[i]], add_len[i], tag[vector_index],
                                            GCM_DEFAULT_TAG_SIZE, ct[vector_index], buf);
            }

            if (ret != 0 || memcmp(buf, pt[pt_index[i]], pt_len[i]) != 0)
            {
                if (verbose != 0)
                {
                    macsec_printf("failed\n");
                }

                math_gcm_free(ctx);
                return 1;
            }

            math_gcm_free(ctx);

            if (verbose != 0)
            {
                macsec_printf("passed\n");
            }
        }
    }

    if (verbose != 0)
    {
        macsec_printf("\n");
    }

    return 0;
}

